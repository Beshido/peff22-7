public class Domino {
    public int haut;
    public int bas;
    public int gauche;
    public int droite;
    public int couleur;
    
    public Domino(int haut, int bas, int gauche, int droite, int couleur) {
        this.haut = haut;
        this.bas = bas;
        this.gauche = gauche;
        this.droite = droite;
        this.couleur = couleur;
    }
}
